public class CampusCardUser
{
	private String cardUserName;
	private String cardUserId;
	
	
	
	public void setUserName(String name)
	{
		cardUserName = name;
	}
	
	public String getUserName()
	{
		return cardUserName;
	}
	
	public void setUserId(String id)
	{
		cardUserId = id;
	}
	
	public String getUserId()
	{
		return cardUserId;
	}
}